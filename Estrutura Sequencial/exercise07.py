"""Create code of calculate area of square"""

#Create first variable of received value of side square
sideSquare = int(input("Please input size of side square: "))

#Operation of calculate area of square
areaSquare = (sideSquare * sideSquare)

#Printing value of area Square
print("Area of square is: ", areaSquare)

#Printing value of square double result
print("Area double of square is: ", areaSquare*2)