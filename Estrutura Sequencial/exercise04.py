"""
Create code of received four notes of school bimester and calculate average of notes
"""

#Create first variable and received of note one
noteOne = float(input("You input first note, please: "))

#Create second variable of received note
noteTwo = float(input("You input second note, please: "))

#Create third variable of received note
noteThree = float(input("You input third note, please: "))

#Create four variable of received note
noteFour = float(input("You input four note, please: "))

#Creating operation of calculate average
averageNotesSchool = (noteOne + noteTwo + noteThree + noteFour) / 4

#Printing average calculated of year
print("Your average of notes in school is: ", averageNotesSchool)



